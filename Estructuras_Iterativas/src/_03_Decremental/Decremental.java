package _03_Decremental;

/**
 *
 * @author SA
 * 
   Ejercicio 3:

   Imprimir los numeros del 10 al 1.

 */
public class Decremental {
    public static void main(String[] args) {
        
        int n=10;
        while (n>=1) {
            System.out.println(n);
            n-=1;
            
        }
    }
    
}
